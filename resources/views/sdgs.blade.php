            <h2 class="title"><a href="https://www.youtube.com/watch?v=HoyHomebdOM&t=154s">17項目標</a></h2>
            
            <div class="goal">
                <img src="../images/1.jpg" alt="目標1">
                <p class="title">目標1.消除貧窮</p>
                <p>①經濟支持：提供基本收入保障和社會保護制度，幫助貧困人口獲得經濟資源。</p>
                <p>②教育機會：確保貧困人口獲得平等的教育機會，提高就業能力和生活水平。</p>
                <p>③醫療保障：為貧困人口提供基本醫療服務，減少疾病對生活的影響。</p>
            </div>

            <div class="goal">
                <img src="../images/2.jpg" alt="目標2">
                <p class="title">目標2.消除饑餓</p>
                <p>①可持續農業：推廣可持續的農業技術，提高糧食產量和質量。支持小農戶，增加農業收入。</p>
                <p>②糧食安全：建立糧食儲備系統，確保糧食供應穩定。改善視頻分配網絡，減少浪費。</p>
                <p>③營養改善：推廣均衡飲食知識，解決營養不良問題。特別關注兒童、孕婦和老年人的營養需求。</p>
            </div>

            <div class="goal">
                <img src="../images/3.jpg" alt="目標3">
                <p class="title">目標3.健康與福祉</p>
                <p>①預防保健：加强疾病預防和健康教育，提高公衆健康意識。推廣疫苗接種和定期體檢。</p>
                <p>②醫療服務：提供可負擔的優質醫療服務，縮小城鄉醫療差距。發展遠程醫療，擴大醫療覆蓋範圍。</p>
                <p>③心理健康：重視心理健康，提供心理諮詢服務.消除對心理疾病的社會偏見,促進全面健康。</p>
            </div>

            <div class="goal">
                <img src="../images/4.jpg" alt="目標4">
                <p class="title">目標4.優質教育</p>
                <p>①普及教育:確保所有兒童都能接受免費、公平和優質的中小學教育。消除教育中的性別差距。</p>
                <p>②終身學習：推廣成人教育和職業培訓，提供終身學習機會。鼓勵創新教育模式和在綫學習。</p>
                <p>③教育質量：提高教師素質，改善教學設施和資源。注重培養學生的批判性思維和創新能力。</p>
                <p>④全球公民：推廣可持續發展教育，培養學生的全球公民意識。增進文化多樣性理解和尊重。</p>
            </div>

            <div class="goal">
                <img src="../images/5.jpg" alt="目標5">
                <p class="title">目標5.性別平等</p>
                <p>①消除歧視：消除對婦女和女童的一切形式的歧視。推動性別平等立法和政策執行。</p>
                <p>②經濟賦權：促進婦女平等參與經濟活動，縮小性別工資差距。支持女性創業和職業發展。</p>
                <p>③決策參與：確保婦女充分參與各級決策。增加女性在政治、經濟和公共生活中的代表性。</p>
                <p>④健康權益：保障婦女的生殖健康權益。消除針對婦女的暴力行爲，提供法律保護。</p>
            </div>

            <div class="goal">
                <img src="../images/6.jpg" alt="目標6">
                <p class="title">目標6.清潔飲水和衛生設施</p>
                <p>①安全飲水：確保所有人都能獲得安全、可負擔的飲用水。改善水源保護和水質監測系統。</p>
                <p>②衛生設施：提供適當的衛生設施，消除露天排便現象。推廣個人衛生習慣，預防疾病傳播。</p>
                <p>③水資源管理：實施綜合水資源管理，提高用水效率。保護和恢復與水有關的生態系統。</p>
                <p>④國際合作：加强國際合作，支持發展中國家改善供水和衛生設施。促進水資源技術交流。</p>
            </div>

            <div class="goal">
                <img src="../images/7.jpg" alt="目標7">
                <p class="title">目標7.經濟適用能源</p>
