                <h2>三、如何預防癌症</h2>
                <h3>3.1 病因</h3>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;癌症源于正常细胞转变为肿瘤细胞的多阶段过程，通常从癌前病变发展到恶性肿瘤。这些变化是一个人的遗传因素与三种外部因素相互作用的结果，外部因素包括：</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;①物理致癌物，例如紫外线和电离辐射；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;②化学致癌物，例如石棉、烟草烟雾成分、酒精、黄曲霉毒素（一种食品污染物）和砷（一种饮水污染物）；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;③生物致癌物，例如由某些病毒、细菌或寄生虫引起的感染。</p>    
                <p>&nbsp;&nbsp;&nbsp;&nbsp;世卫组织通过其国际癌症研究机构对致癌因素进行分类。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;癌症发病率随着年龄的增长而急剧上升，这很可能是因为特定癌症的风险随着年龄的增长而增加。随着年龄的增长，整体风险不断积累，而同时细胞修复机制的有效性则呈下降趋势。</p>
            </article>

            <article>
                <h3>3.2 預防</h3>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;通過前面提到的病因，我們可以得知降低癌症风险的方式包括以下：</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;①不使用烟草；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;②保持健康的体重；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;③健康饮食，包括水果和蔬菜；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;④经常进行身体活动；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;⑤避免或减少酒精消费；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;⑥如果属于建议接种疫苗的群体，则请接种人乳头瘤病毒疫苗和乙肝疫苗；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;⑦避免暴露于紫外线辐射（这主要源于阳光暴晒和人工晒黑设备)和/或采取防晒措施；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;⑧确保在卫生保健中安全和适当地使用辐射（用于诊断和治疗目的）；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;⑨尽量减少在职业中接触电离辐射；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;⑩减少接触室外和室内空气污染，包括氡（一种由铀的自然衰变产生的放射性气体，可能在建筑物中，住宅、学校和工作场所积累）。</p>
            </article>
